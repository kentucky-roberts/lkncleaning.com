          <h3>Types of Carpet</h3>
          <p>
            <strong>Nylon:</strong> Nylon is very durable, resilient and soil-resist it. It offers good colorfastness. Nylon tends to stain easily and dries slowly after cleaning.<br/>

            <strong>Olefin:</strong> Olefin is naturally stain, fade and moisture-resistant and is extremely colorfast. It cleans easily and dries very quickly compared to most fibers.<br/>

            <strong>Polyester:</strong> Polyester is naturally stain and fade-resistant and offers exceptional softness in color clarity. Polyester is not as resilient as nylon and yellows with repeated exposure to petroleum-based spotters.<br/>

            <strong>Acrylic:</strong> Popular fiber in the 1960's since it looks and feels like wool. It tends to fill (fuzz), so it is seldom used.<br/>

            <strong>Wool:</strong> Wool is a natural fiber that does not offer the same durability and resilience as nylon. Wool is not exceptionally stain or soil-resistant and should only be cleaned with natural pH detergents. Always avoid excess moisture when cleaning.<br/>
          </p>



